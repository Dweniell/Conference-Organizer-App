class User{
    Id=1;
    Name
    Surname
    Permission = {
        AUTHOR: 'A',
        REVIEWER: 'R',
        ORGANIZER: 'O'
    }
    Password

    constructor(Id, Name, Surname,Permission,Password){
        this.Id=Id;
        this.Name=Name;
        this.Surname=Surname;
        this.Permission=Permission;
        this.Password=Password;
    }

    
}